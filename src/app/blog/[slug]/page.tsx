export async function generateStaticParams() {
  return Object.keys(articles).map(slug => ({ slug }));
}

export async function generateMetadata({ params }: { params: { slug: string } }) {
  const article = articles[params.slug];
  if (!article) return {};
  return {
    title: `${article.title} | LingoForge Blog`,
    description: article.content.substring(0, 160).replace(/[#*\[\]]/g, ''),
  };
}

export default function ArticlePage({ params }: { params: { slug: string } }) {
  const article = articles[params.slug];
  if (!article) notFound();

  const paragraphs = article.content.split('\n').filter((l: string) => l.trim());

  return (
    <div className="flex min-h-dvh flex-col bg-background">
      <Navigation />
      <main className="flex-1 container mx-auto px-4 py-12 max-w-3xl">
        <Link href="/blog" className="text-sm text-muted-foreground hover:text-foreground mb-6 block">
          ← Back to Blog
        </Link>

        <div className="flex flex-wrap gap-2 mb-4">
          {article.tags.map((tag: string) => (
            <span key={tag} className="text-xs bg-primary/10 text-primary px-2 py-1 rounded-full">{tag}</span>
          ))}
        </div>

        <h1 className="text-2xl font-bold mb-2">{article.title}</h1>
        <p className="text-muted-foreground text-sm mb-6">{article.titleSi}</p>

        <div className="flex items-center gap-4 text-xs text-muted-foreground mb-8 pb-6 border-b border-border">
          <span>📅 {article.date}</span>
          <span>⏱️ {article.readTime} read</span>
        </div>

        <article className="prose prose-invert max-w-none">
          {paragraphs.map((para: string, i: number) => {
            if (para.startsWith('## ')) return <h2 key={i} className="text-xl font-bold mt-8 mb-4">{para.replace('## ', '')}</h2>;
            if (para.startsWith('### ')) return <h3 key={i} className="text-lg font-semibold mt-6 mb-3">{para.replace('### ', '')}</h3>;
            if (para.startsWith('- ')) return <li key={i} className="ml-4 text-muted-foreground">{para.replace('- ', '')}</li>;
            if (para.startsWith('✅')) return <p key={i} className="text-sm text-muted-foreground">{para}</p>;
            if (para.startsWith('[')) {
              const match = para.match(/\[(.+)\]\((.+)\)/);
              if (match) return <Link key={i} href={match[2]} className="block mt-4 bg-primary text-primary-foreground px-6 py-3 rounded-lg text-center font-semibold hover:bg-primary/90">{match[1]}</Link>;
            }
            if (para.startsWith('**') && para.endsWith('**')) return <p key={i} className="font-semibold mt-4">{para.replace(/\*\*/g, '')}</p>;
            return <p key={i} className="text-muted-foreground leading-7 mt-3">{para}</p>;
          })}
        </article>

        <div className="mt-12 p-6 bg-card border border-border rounded-xl text-center">
          <h3 className="font-bold text-lg mb-2">Start Learning Today</h3>
          <p className="text-muted-foreground text-sm mb-4">Week 1 completely FREE. No credit card required.</p>
          <Link href="/login" className="bg-primary text-primary-foreground px-8 py-3 rounded-lg font-semibold hover:bg-primary/90 inline-block">
            Start Free →
          </Link>
        </div>
      </main>
    </div>
  );
}
